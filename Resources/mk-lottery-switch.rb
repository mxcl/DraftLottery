day_of_month_lines = DATA.each_line.to_a

# lose month labels
day_of_month_lines.shift

# convert to array of arrays of lottery numbers
tickets = day_of_month_lines.map do |month_line|
  month_line.split' '
end

# convert to chronological order
tickets = tickets.transpose

# lose day of month labels
tickets.shift

tickets = tickets.flatten.reject{|num| num == '---'}.map(&:to_i)

abort("Invalid ticket count: #{tickets.uniq.count}") unless tickets.uniq.count == 366

tickets.each_with_index do |ticket, index|
  puts "case #{index}: return #{ticket}"
end

